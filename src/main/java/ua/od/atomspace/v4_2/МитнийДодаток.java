package ua.od.atomspace.v4_2;

import ua.od.atomspace.v4_2.конфігурація.ФабрикаЕкземплярів;
import ua.od.atomspace.v4_2.модель.ВідповідьМитніці;
import ua.od.atomspace.v4_2.модель.Людина;
import ua.od.atomspace.v4_2.сервіс.*;

public class МитнийДодаток {
    public static void main(String[] args) {
        ФабрикаЕкземплярів фабрикаЕкземплярів = ФабрикаЕкземплярів.Одинак.взятиЕкзкмпляр();
        // зчитати людину
        ЧергаНаКордоні чергаНаКордоні = фабрикаЕкземплярів.взятиЕкземпляр(ЧергаНаКордоніМолдови.class);
        Людина людина = чергаНаКордоні.забратиКрайнього();

        // перевірити людину
        Митниця митниця = фабрикаЕкземплярів.взятиЕкземпляр(МитницяОдеси.class);
        ВідповідьМитніці відповідьМитніці = митниця.обробитиЛюдину(людина);

        //впустити до країни, або депортувати
        МіграційнаСлужба міграційнаСлужба = фабрикаЕкземплярів.взятиЕкземпляр(МіграційнаСлужбаУкраїни.class);
        міграційнаСлужба.мігрувати(відповідьМитніці);
    }
}
